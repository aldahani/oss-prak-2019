a = 5
b = 3

# penjumlahan
c = a + b
print("hasil a + b = ", c)

# pengurangan
c = a - b
print("hasil a - b = ", c)

# perkalian
c = a*b
print("hasil a*b = ", c)

# pembagian
c = a / b
print("hasil a/b =", c)

# pembagian pembulatan keatas
c = a / b
c = round(c)
print("hasil a/b = ",c)

# modulus
c = a % b
print("hasil a % b = ", c)

# pangkat
c = a ** b
print("hasil a ** b = ", c)
